namespace Operations;

/// <summary>
/// Basic ariphmetic operations.
/// </summary>
public enum Operations : int
{
    /// <summary>
    /// Representation of the addition operation.
    /// </summary>
    Addition = '+',

    /// <summary>
    /// Representation of the substraction operation.
    /// </summary>
    Substraction = '-',

    /// <summary>
    /// Representation of the multiplication operation.
    /// </summary>
    Multiplication = '*',

    /// <summary>
    /// Representation of the division operation.
    /// </summary>
    Division = '/',
}
